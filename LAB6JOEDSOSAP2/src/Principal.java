    private void agregarperMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregarperMouseClicked
        DefaultTreeModel modelo = (DefaultTreeModel) jt_personajes.getModel();
        DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) modelo.getRoot();
        DefaultMutableTreeNode nodo_universo = (DefaultMutableTreeNode) raiz.getChildAt(cb_universo.getSelectedIndex());
        DefaultMutableTreeNode nodo_personaje;
        nodo_personaje = new DefaultMutableTreeNode(new Personajes(tf_nombre.getText(), 1));
        nodo_universo.add(nodo_personaje);
        String cbuni = cb_universo.getSelectedItem().toString();

        personaje.add(new Personajes(tf_nombre.getText(), tf_poder.getText(), tf_debilidad.getText(), cbuni, (Integer) sp_fuerza.getValue(), (Integer) sp_agilidadfis.getValue(), (Integer) sp_agilidadmen.getValue(), (Integer) sp_hp.getValue()));
        JOptionPane.showMessageDialog(null, "Personaje agregado exitosamente!");
        tf_name.setText("");
        tf_poder.setText("");
        tf_debilidad.setText("");
        
        Agregar.setVisible(false);
        this.setVisible(true);
        this.pack();//le pone el tamanio original a la subventana 
        this.setLocationRelativeTo(this);

    }//GEN-LAST:event_agregarperMouseClicked

