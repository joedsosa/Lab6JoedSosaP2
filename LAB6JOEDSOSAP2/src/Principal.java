    private void jt_personajesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_personajesMouseClicked
        // TODO add your handling code here:
       if (evt.isMetaDown()) {
            
            int row = jt_personajes.getClosestRowForLocation(evt.getX(),  evt.getY());
            jt_personajes.setSelectionRow(row);          
            Object v1= jt_personajes.getSelectionPath().getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;
            
            if (nodo_seleccionado.getUserObject() instanceof Personajes) {
                persona_seleccionada = (Personajes) nodo_seleccionado.getUserObject();
                pop_uni.show(evt.getComponent(), evt.getX(), evt.getY());
            }else if (!nodo_seleccionado.getUserObject().equals("Personajes")) {
               DefaultListModel modelo  = (DefaultListModel) jl_personajes.getModel();
               modelo.clear();
               for (Personajes p : personaje) {
                    if (p.getUniverso().equals(nodo_seleccionado.getUserObject())) {
                        modelo.addElement(personaje.get(personaje.indexOf(p)).getNombre());
                    }
                }
            }
}
    }//GEN-LAST:event_jt_personajesMouseClicked

